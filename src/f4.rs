//! F4-style Gröbner basis driver.
//!
//! Phase L delivered a sparse [`crate::matrix::Matrix`] +
//! [`crate::matrix::rref`] reducer; this module wires those into a
//! Faugère-F4-style outer loop that batches S-pairs at the same total
//! degree, builds one big sparse matrix, and reduces it in a single
//! RREF call instead of running the per-pair geobucket reducer used by
//! [`crate::bba::compute_gb_serial`].
//!
//! The algorithm follows Faugère's original F4 [`F4'99`]:
//!
//! 1. **Selection.** Pop every still-pending pair whose lcm has the
//!    smallest total degree.
//! 2. **Symbolic preprocessing.** For each S-pair `(f, g)` with
//!    `lcm = lcm(lm f, lm g)`, add the shifted polys `(lcm/lm f) · f`
//!    and `(lcm/lm g) · g` as matrix rows. For every monomial that
//!    appears in any row but is not yet a "covered" leading monomial,
//!    search the basis for a divisor and add the corresponding shifted
//!    reductor as another row. Iterate until no new monomials remain.
//! 3. **Reduction.** Sort all monomials descending in `M::cmp`, build
//!    a [`Matrix`] indexed by that order, and call
//!    [`crate::matrix::rref`].
//! 4. **Harvest.** Any non-zero RREF row whose leading column was
//!    *not* a leading column of an input row is a new basis element.
//!    Add it (monic-normalised) to the basis and re-enqueue pairs.
//! 5. **Inter-reduce.** Once all pairs are exhausted, tail-reduce the
//!    surviving basis to obtain the unique reduced GB.
//!
//! [`F4'99`]: J.-C. Faugère, "A new efficient algorithm for computing
//!   Gröbner bases (F4)", J. Pure Appl. Algebra 139 (1999) 61–88.
//!
//! ## Scope
//!
//! * Single-threaded.
//! * No Gebauer–Möller chain criterion (relies on the matrix to
//!   discover redundancy). A future phase can layer GM on top.
//! * Validated against [`crate::compute_gb`] and
//!   [`crate::validate::is_groebner_basis`] in the unit tests.
//!
//! Output is **the** reduced Gröbner basis, in the same canonical
//! ascending-by-leading-monomial order as
//! [`crate::bba::compute_gb_serial`].

use std::cmp::Reverse;
use std::collections::{BinaryHeap, HashMap, HashSet};
use std::sync::Arc;

use crate::field::Field;
