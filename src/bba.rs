//! bba — sequential Gröbner-basis driver.
//!
//! Public entry point: [`compute_gb`]. Given an input ideal as a
//! `Vec<Poly>`, it computes the reduced Gröbner basis and returns it
//! in a deterministic, canonical order (ascending by leading
//! monomial under the ring's degrevlex — matches Singular's
//! `sort(G)[1]` convention so the text fixtures compare cleanly).
//!
//! The algorithm is the classical Buchberger loop with Gebauer–Möller
//! pair criteria, using the data structures already in the crate:
//! [`SBasis`] for the growing basis, [`LSet`] for the pair queue, the
//! `gm` module for `enter_s` / `enterpairs`, [`LObject`] as the
//! reducer transient, and the geobucket [`KBucket`] inside the
//! LObject.
//!
//! Reference: `~/Singular/kernel/GBEngine/kstd2.cc`'s `bba()` for the
//! serial homogeneous path, and `redtailBba` for the tail-reduction
//! pass. No code copied; algorithms re-derived.
//!
//! Scope: single-threaded, degrevlex only, `redHomog` semantics. No
//! ecart, no Mora, no signature-based reduction. Output is **the**
//! reduced Gröbner basis — unique up to permutation, and the
//! canonical sort at the end makes the permutation deterministic too.

use std::sync::Arc;

use crate::field::Field;
use crate::gm::enterpairs;
use crate::kbucket::KBucket;
use crate::lobject::LObject;
use crate::lset::LSet;
use crate::ordering::MonoOrder;
use crate::poly::Poly;
use crate::ring::Ring;
use crate::sbasis::SBasis;

/// Compute the reduced Gröbner basis of the ideal generated by
/// `input`, over the ring `ring`.
///
/// * Zero polynomials in `input` are discarded.
/// * An input containing a nonzero constant collapses to `[1]`.
/// * The returned list is in canonical order: **ascending** by
///   leading monomial under the ring's ordering. This makes the
///   output a deterministic function of the ideal, independent of
///   the order in which generators appear in `input`.
///
/// Thread count: controlled by the `ARK_GB_THREADS` environment
/// variable. Default 1 (serial path). Values `>= 2` dispatch to
/// [`crate::parallel::compute_gb_parallel`], which runs a
/// continuous-cursor sweep with worker-side drain.
///
/// At `T == 1` this runs the serial path, which is deterministic
/// bit-for-bit (matches pre-parallel behaviour). At `T > 1` the
/// output is still the reduced Gröbner basis — unique up to
/// permutation — and the canonical sort removes the permutation
/// ambiguity, so the output is still a function of the ideal.
pub fn compute_gb<F: Field + Copy + Send + Sync + 'static, O: MonoOrder + 'static>(
    ring: Arc<Ring<F, O>>,
    input: Vec<Poly<F>>,
) -> Vec<Poly<F>> {
    let num_threads = ark_gb_threads();
    if num_threads == 1 {
        compute_gb_serial(ring, input)
    } else {
        // Cancellation at the env-var-level dispatch surface has no
        // handle caller — we `expect` the computation to complete.
        // For cancellation via an external signal, callers should
        // use `parallel::compute_gb_parallel` directly with their
        // own `Computation` instance (forthcoming work once the FFI
        // cancel hook lands).
        crate::parallel::compute_gb_parallel(ring, input, num_threads)
            .expect("compute_gb cancelled with no external cancel source")
    }
}

/// Parse `ARK_GB_THREADS` from the environment. Default 1. Any
/// non-numeric or negative value parses to 1. Cap at 256 so a user
/// typo (`ARK_GB_THREADS=1000000`) doesn't explode the thread pool.
fn ark_gb_threads() -> usize {
    match std::env::var("ARK_GB_THREADS") {
        Ok(v) => v.parse::<usize>().unwrap_or(1).clamp(1, 256),
        Err(_) => 1,
    }
}

/// The serial implementation of `compute_gb`. Exposed so the
/// env-var dispatch can pick between serial and parallel without
/// changing the public API.
pub fn compute_gb_serial<F: Field + Copy + Send + Sync + 'static, O: MonoOrder + 'static>(
    ring: Arc<Ring<F, O>>,
    input: Vec<Poly<F>>,
) -> Vec<Poly<F>> {
    let mut s_basis = SBasis::new();
    let mut l_set = LSet::new();
    let mut next_arrival: u64 = 0;

    // Seed: reduce each nonzero input against the already-accepted
    // basis (so its leading term isn't already covered), make monic,
    // and feed it through the pipeline.
    //
    // Without this pre-reduction pass, redundant input generators
    // (e.g. f_1 = xy+yz+zx when f_0 = x+y+z is already in the
    // basis — x | xy) would stay in the basis all the way to the
    // end, because the SBasis redundancy marking only triggers when
    // the *newer* element's LM divides an older one, not vice versa.
    // Singular's bba() pre-reduces inputs via the same reducer loop.
    for p in input {
        if p.is_zero() {
            continue;
        }
        let sugar = p.lm_deg();
        let mut lobj = LObject::from_poly_with_sugar(Arc::clone(&ring), p, sugar);
        reduce_lobject(&mut lobj, &s_basis, &ring);
        if lobj.is_zero() {
            continue;
        }
        let h_sugar = lobj.sugar();
        let h = lobj
            .into_poly()
            .monic(&ring)
            .expect("nonzero poly has invertible lc");
        next_arrival = insert_and_generate_pairs_with_sugar(
            &ring,
            &mut s_basis,
            &mut l_set,
            h,
            h_sugar,
            next_arrival,
        );
    }

    // Main loop: pop pairs, reduce, insert survivors.
    //
    // Note: we DO NOT skip pairs where an index is redundant. The
    // pair's S-polynomial is still meaningful — the fact that
    // `S[i]`'s leading monomial is divisible by some later element's
    // LM doesn't make the S-pair vanish, it just means the reducer
    // will find a divisor on the first step and start reducing.
    // Singular's `bba()` skips a pair only when its polynomial was
    // physically deleted; our design preserves pointers, so there's
    // nothing to skip here.
    while let Some(pair) = l_set.pop() {
        let s_i = s_basis.poly(pair.i as usize);
        let s_j = s_basis.poly(pair.j as usize);
        let mut lobj = match LObject::from_spoly(Arc::clone(&ring), s_i, s_j, &pair) {
            Some(l) => l,
            None => continue,
        };
        reduce_lobject(&mut lobj, &s_basis, &ring);
        if lobj.is_zero() {
            continue;
        }
        let h_sugar = lobj.sugar();
        let h = lobj
            .into_poly()
            .monic(&ring)
            .expect("nonzero lobject has invertible lc");
        next_arrival = insert_and_generate_pairs_with_sugar(
            &ring,
            &mut s_basis,
            &mut l_set,
            h,
            h_sugar,
            next_arrival,
        );
    }

    // Tail-reduce every surviving basis element against the rest of
    // the basis to obtain the unique reduced GB.
    tail_reduce_all(&mut s_basis, &ring);

    // Extract the surviving (non-redundant) polynomials, canonically
    // sorted. We sort **ascending** by leading monomial to match
    // Singular's `sort(G)[1]` convention (which "smaller polys come
    // first" calls out — the fixture files are in this order too).
    let mut out: Vec<Poly<F>> = s_basis.iter_active().map(|(_, p)| p.clone()).collect();
    out.sort_by(|a, b| {
        let lm_a = a.leading().expect("active basis element is nonzero").1;
        let lm_b = b.leading().expect("active basis element is nonzero").1;
        lm_a.cmp(lm_b, &ring)
    });
    out
}

/// Insert `h` into `s_basis` with an explicit sugar, then run the
/// full `enterpairs` pipeline (pair generation + chain criterion +
/// merge into L). Returns the updated `next_arrival`.
///
/// Important sequencing: `insert_no_clear` runs first, then
/// `enterpairs` (which can now see every not-yet-redundant older
/// element), then `clear_redundant_for` runs to mark older LMs that
/// `h`'s LM divides as redundant. This matches Singular's
/// `initenterpairs` + `clearS` ordering in `kutil.cc::enterpairs`.
/// Reversing the order would suppress pairs against siblings that
/// `h` is about to make redundant, losing relations that the bba
/// would otherwise produce.
///
/// Pairs in L that reference a now-redundant index are *not*
/// purged. They stay in L and are still popped. The main-loop
/// reducer handles them correctly: the first reduction step will
/// find the dividing element (which just arrived) and reduce the
/// leader through it, so the pair still produces the right
/// reduction. See the comment in `compute_gb`'s main loop for why
/// this is the right contract.
fn insert_and_generate_pairs_with_sugar<F: Field + Copy + Send + Sync, O: MonoOrder>(
    ring: &Ring<F, O>,
    s_basis: &mut SBasis<F>,
    l_set: &mut LSet,
    h: Poly<F>,
    sugar: u32,
    next_arrival: u64,
) -> u64 {
    let h_idx = s_basis.insert_no_clear(h) as u32;
    let h_ref = s_basis.poly(h_idx as usize);
    let created = enterpairs(ring, s_basis, h_idx, h_ref, sugar, l_set, next_arrival);
    s_basis.clear_redundant_for(ring, h_idx as usize);
    next_arrival + created as u64
}

/// Reduce `lobj` against `s_basis` until no basis element's leading
/// monomial divides the current leader. First-divisor-wins with a
/// sev pre-filter (same pattern as Singular's `kFindDivisibleByInS`).
///
/// Sugar is tracked: after reducing by `s_basis[idx]` with multiplier
/// `m`, the LObject's sugar becomes
/// `max(old_sugar, lm_deg(s_basis[idx]) + deg(m))`.
/// Reduce `lobj` to normal form against `s_basis`.
///
/// Dispatches to one of two reducer implementations based on the
/// `heap_reducer` cargo feature:
/// * default (geobucket): [`reduce_lobject_geobucket`] — the
///   ADR-002 reducer architecture, in production use through
///   v5 of the optimisation series.
/// * `--features heap_reducer`: [`reduce_lobject_heap`] — the
///   ADR-008 heap-based Monagan-Pearce reducer.
///
/// Both implementations are always compiled (feature-gated only
/// at the dispatch site, not at the function definition). The
/// cargo test suite cross-validates them via the
/// `geobucket_and_heap_reducer_agree` test below.
fn reduce_lobject<F: Field + Copy + Send + Sync, O: MonoOrder>(
    lobj: &mut LObject<F, O>,
    s_basis: &SBasis<F>,
    ring: &Arc<Ring<F, O>>,
) {
    #[cfg(not(feature = "heap_reducer"))]
    reduce_lobject_geobucket(lobj, s_basis, ring);
    #[cfg(feature = "heap_reducer")]
    reduce_lobject_heap(lobj, s_basis, ring);
}

/// Geobucket-based reducer (ADR-002). Performs the reduction
/// in place using the LObject's internal `KBucket`. Each
/// reduction step calls `bucket.minus_m_mult_p` to subtract
/// `c * m * s` and `bucket.refresh()` to recompute the leader.
///
/// This is the default reducer (compiled into `reduce_lobject`
/// when `--features heap_reducer` is *not* set).
pub fn reduce_lobject_geobucket<F: Field + Copy + Send + Sync, O: MonoOrder>(
    lobj: &mut LObject<F, O>,
    s_basis: &SBasis<F>,
    ring: &Arc<Ring<F, O>>,
) {
    loop {
        if lobj.is_zero() {
            return;
        }
        // Take a snapshot of the leading monomial & coeff to drive
        // the divisor search.
        let lm_sev = lobj.lm_sev();
        let lm_coeff = lobj.lm_coeff();
        // `lobj.leading()` borrows the bucket mutably — we need to
        // collect the monomial value before running the divisor
        // search over the S-basis.
        let lm = *lobj.leading().expect("non-zero lobject has leading").1;

        let Some(idx) = find_divisor_idx(s_basis, lm_sev, &lm, ring) else {
            return;
        };

        // Perform the reduction step `lobj -= (lm_coeff / s_lc) * m * s`.
        // Every basis element was `monic()`-normalised at insert time,
        // so `s_lc == 1` and `inv_s_lc == 1`; we can skip the Fermat
        // inversion entirely. `c` is just the LObject's leading
        // coefficient.
        let s = s_basis.poly(idx);
        let (s_lc, s_lm_ref) = s.leading().expect("non-redundant basis is nonzero");
        debug_assert!(s_lc.is_one(), "basis element should be monic");
        let _ = s_lc;
        let m = lm
            .div(s_lm_ref, ring)
            .expect("divisibility already checked");
        let m_deg = m.total_deg();
        let c = lm_coeff;

        lobj.bucket_mut().minus_m_mult_p(&m, c, s);
        lobj.refresh();

        // Sugar update: max(old, sugar(s) + deg(m)). For this driver
        // we use lm_deg(s) as the basis element's sugar. Propagate
        // via `set_sugar`.
        let s_sugar = s_basis.lm_degs()[idx];
        let new_sugar = lobj.sugar().max(s_sugar + m_deg);
        lobj.set_sugar(new_sugar);
    }
}

/// Heap-based Monagan-Pearce reducer (ADR-008). Extracts the
/// LObject's current poly via `into_poly`, drives the reduction
/// through a [`crate::reducer::ReducerHeap`], and rebuilds the
/// LObject from the surviving result.
///
/// Compiled into `reduce_lobject` when `--features heap_reducer`
/// is set. Always compiled into the crate for the
/// `geobucket_and_heap_reducer_agree` cross-validation test.
///
/// The geobucket-extraction step costs one extra `Poly`
/// construction per reduction. If the v6 profile shows that as
/// significant, the deferred LObject restructure (see ADR-008)
/// would eliminate it.
pub fn reduce_lobject_heap<F: Field + Copy + Send + Sync, O: MonoOrder>(
    lobj: &mut LObject<F, O>,
    s_basis: &SBasis<F>,
    ring: &Arc<Ring<F, O>>,
) {
    use crate::reducer::{Reducer, ReducerHeap};

    if lobj.is_zero() {
        return;
    }

    // Extract the LObject's current state. We mem::replace with
    // a placeholder so we can take ownership of the bucket; the
    // placeholder is dropped almost immediately when we replace
    // again at the end.
    let initial_sugar = lobj.sugar();
    let placeholder = LObject::from_poly_with_sugar(Arc::clone(ring), Poly::zero(), 0);
    let consumed = std::mem::replace(lobj, placeholder);
    let initial_poly = consumed.into_poly();
    if initial_poly.is_zero() {
        // Edge case: lobj was reported non-zero but the bucket
        // collapsed to zero on extraction. Leave lobj as the
        // placeholder (already zero).
        return;
    }

    // Build the reducer state and seed it with the initial poly
    // as the first reducer (multiplier = 1, coeff = 1).
    let mut h = ReducerHeap::new(Arc::clone(ring), initial_sugar);
    h.push_reducer(Reducer {
        poly: &initial_poly,
        multiplier: crate::monomial::MonoTerm::one(ring),
        coeff: F::one(),
        cursor: initial_poly.cursor(),
        sugar: initial_sugar,
    });

    // Drive the reduction. The closure adapts our existing
    // `find_divisor_idx` (the same one the geobucket path uses,
    // including the AVX2-batched sev pre-filter from ADR-007)
    // into the callback shape ReducerHeap::reduce_to_normal_form
    // expects.
    let (result, final_sugar) = h.reduce_to_normal_form(|leader| {
        find_divisor_idx(s_basis, leader.sev(), leader, ring)
            .map(|idx| (s_basis.poly(idx), s_basis.lm_degs()[idx]))
    });

    // Replace the LObject with one carrying the heap reducer's
    // result + final sugar.
    *lobj = LObject::from_poly_with_sugar(Arc::clone(ring), result, final_sugar);
}

/// Find the index of a basis element whose leading monomial divides
/// `lm`, skipping redundant entries. Returns the first match in
/// insertion order, or `None`.
///
/// Internally dispatches to a SIMD-batched sev pre-filter when AVX2
/// is enabled at compile time, and a scalar walk otherwise. The two
/// paths produce identical results on identical inputs (verified by
/// `find_divisor_idx_simd_matches_scalar` in the unit tests).
///
/// See `~/ark_gb/docs/design-decisions.md` ADR-007 for the rationale.
#[inline]
fn find_divisor_idx<F: Field + Copy + Send + Sync, O: MonoOrder>(
    s_basis: &SBasis<F>,
    lm_sev: u64,
    lm: &crate::monomial::MonoTerm,
    ring: &Ring<F, O>,
) -> Option<usize> {
    let sevs = s_basis.sevs();
    let lms = s_basis.lms();
    let redund = s_basis.redundant_flags();
    let len = sevs.len();
    let not_lm_sev = !lm_sev;

    let mut idx = 0;
    while idx < len {
        // Find the next sev that passes the pre-filter
        // (`(sevs[idx] & not_lm_sev) == 0`). Returns `len` if none.
        idx = find_sev_match(sevs, not_lm_sev, idx);
        if idx >= len {
            return None;
        }
        // Sev passes; now check redundant flag and the actual divides.
        // ADR-010: read the leading monomial from the lms cache
        // (flat Vec<MonoTerm>, contiguous) instead of dereferencing
        // s_basis.poly(idx) (a Box<Poly> with separately-allocated
        // memory). Eliminates the L1/L2 miss that the v7 perf annotate
        // showed at 11 % of within-function cycles in
        // reduce_to_normal_form.
        if !redund[idx] && lms[idx].divides(lm, ring) {
            return Some(idx);
        }
        idx += 1;
    }
    None
}

// `find_sev_match` (and its AVX2 / scalar implementations) live
// in `crate::simd` as of ADR-009 so they can be reused from
// `gm::chain_crit_normal`. See `~/ark_gb/src/simd.rs`.
use crate::simd::find_sev_match;

/// Walk the basis and tail-reduce every non-redundant element against
/// the others. This is the final pass that turns a Gröbner basis
/// into **the** reduced Gröbner basis.
///
/// Strategy (mirrors Singular's `redtailBba`): for each basis index
/// `i`, extract `f = basis[i]`, temporarily mark `i` redundant, build
/// a bucket that holds only `f`'s tail (`f` without its leading
/// term), reduce the tail to normal form against the rest of the
/// basis, re-attach the original leading term, make monic, and put
/// the result back at index `i`. The `i`-redundant flag is cleared
/// unless `f`'s leading monomial was itself reducible by a *later*
/// basis element (in which case `f` really is redundant — though in
/// practice `enter_s` has already marked those cases).
///
/// This preserves the basis's leading monomials. Two successive
/// tail-reduction passes are idempotent: the output of the first
/// pass has all tail terms normal-form-reduced, so the second pass
/// finds nothing further to do.
fn tail_reduce_all<F: Field + Copy + Send + Sync, O: MonoOrder>(
    s_basis: &mut SBasis<F>,
    ring: &Arc<Ring<F, O>>,
) {
    let n = s_basis.len();
    for i in 0..n {
        if s_basis.is_redundant(i) {
            continue;
        }
        let f = s_basis.poly(i).clone();
        if f.len() <= 1 {
            // Single-term poly: nothing to tail-reduce. Still make
            // monic in case it wasn't already.
            let monic = f.monic(ring).expect("nonzero poly");
            s_basis.replace_poly(ring, i, monic);
            continue;
        }

        // Split f into leading term and tail. We'll reduce the tail
        // as a bucket, then re-attach the leading term.
        let (lc, lm) = {
            let (c, m) = f.leading().expect("nonzero");
            (c, *m)
        };
        let tail = f.drop_leading();

        // Mark `i` redundant during the reduction so we don't
        // accidentally reduce by ourselves.
        s_basis.set_redundant(i, true);

        let reduced_tail = reduce_tail(tail, s_basis, ring);

        // Unmark.
        s_basis.set_redundant(i, false);

        // Re-attach leading term and make monic.
        let combined = prepend_leading(lc, &lm, reduced_tail, ring);
        let monic = combined.monic(ring).expect("nonzero poly");
        s_basis.replace_poly(ring, i, monic);
    }
}

/// Reduce a tail polynomial against `s_basis` to full normal form —
/// every term is reduced, not just the leading one. Returns the
/// resulting polynomial (possibly zero).
///
/// Algorithm: hold the working polynomial in a [`KBucket`]. Peel the
/// leader at each step; if it has a divisor in the basis, run a
/// `minus_m_mult_p` that subtracts `(lc/s_lc) * (lm/s_lm) * s` from
/// the bucket (this cancels the leader and mixes the tail of the
/// multiplier back in). If it has no divisor, park the term into
/// `done` and re-scan the bucket for its new leader. The parked
/// terms accumulate in strict descending order because
/// `extract_leading` always yields the bucket's current max.
fn reduce_tail<F: Field + Copy + Send + Sync, O: MonoOrder>(
    tail: Poly<F>,
    s_basis: &SBasis<F>,
    ring: &Arc<Ring<F, O>>,
) -> Poly<F> {
    if tail.is_zero() {
        return tail;
    }
    let mut bucket = KBucket::from_poly(Arc::clone(ring), tail);
    let mut done: Vec<(F, crate::monomial::MonoTerm)> = Vec::new();

    // clippy::while_let_loop would suggest a `while let Some((c,
    // m_ref)) = bucket.leading() { .. }`, but `bucket.leading()`
    // returns a reference tied to `bucket`, which would conflict with
    // the mutable bucket operations we need to do inside the loop.
    // The explicit `let ... else break` pattern releases the borrow
    // before we touch the bucket again.
    #[allow(clippy::while_let_loop)]
    loop {
        // Peek at the bucket's leader.
        let Some((c, m_ref)) = bucket.leading() else {
            break;
        };
        let m = *m_ref;
        let lm_sev = m.sev();

        // Sev pre-filter + real divisibility check.
        let sevs = s_basis.sevs();
        let redund = s_basis.redundant_flags();
        let mut divisor: Option<usize> = None;
        for idx in 0..s_basis.len() {
            if redund[idx] {
                continue;
            }
            let s_sev = sevs[idx];
            if (s_sev & !lm_sev) != 0 {
                continue;
            }
            let s_lm = s_basis
                .poly(idx)
                .leading()
                .expect("non-redundant basis element is nonzero")
                .1;
            if s_lm.divides(&m, ring) {
                divisor = Some(idx);
                break;
            }
        }
        match divisor {
            None => {
                // Term is in normal form; extract it and park.
                let (pc, pm) = bucket.extract_leading().expect("just peeked, it's there");
                debug_assert_eq!(pc, c);
                done.push((pc, pm));
            }
            Some(idx) => {
                // Reduce: bucket -= (c / s_lc) * (m / s_lm) * s.
                // Basis elements are monic (`s_lc == 1`), so
                // `(c / s_lc) == c` and we skip the Fermat inversion.
                let s = s_basis.poly(idx);
                let (s_lc, s_lm_ref) = s.leading().expect("non-redundant");
                debug_assert!(s_lc.is_one(), "basis element should be monic");
                let _ = s_lc;
                let mult = m.div(s_lm_ref, ring).expect("divisibility checked");
                bucket.minus_m_mult_p(&mult, c, s);
            }
        }
    }

    // `done` holds the normal-form terms in strictly descending order
    // (each term came from `bucket.extract_leading`, which always
    // yields the current maximum). Use the descending-terms fast path
    // to skip the re-sort in `Poly::from_terms`.
    if done.is_empty() {
        Poly::zero()
    } else {
        Poly::from_descending_terms_unchecked(ring, done)
    }
}

/// Build a polynomial with leading term `(lc, lm)` followed by the
/// terms of `tail`. Precondition: `tail` is a canonical polynomial
/// whose every term has a monomial strictly less than `lm` under the
/// ring's ordering. `(lc, lm)` is prepended and the resulting vector
/// is already in strictly-descending order with no duplicates, so we
/// take the fast path and skip the sort.
fn prepend_leading<F: Field + Copy + Send + Sync, O: MonoOrder>(
    lc: F,
    lm: &crate::monomial::MonoTerm,
    tail: Poly<F>,
    ring: &Ring<F, O>,
) -> Poly<F> {
    let mut terms: Vec<(F, crate::monomial::MonoTerm)> = Vec::with_capacity(tail.len() + 1);
    terms.push((lc, *lm));
    for (c, m) in tail.iter() {
        terms.push((c, *m));
    }
    Poly::from_descending_terms_unchecked(ring, terms)
}

