    /// ADR-008 cross-validation: the geobucket reducer
    /// (`reduce_lobject_geobucket`) and the heap reducer
    /// (`reduce_lobject_heap`) must produce algorithmically
    /// equivalent results on the same input. They are
    /// expected to give bit-for-bit identical surviving polys
    /// (modulo canonical form), since both implement the
    /// same reduction-to-normal-form algorithm against the
    /// same monic basis.
    ///
    /// Test fixtures: hand-built (basis, lobject_poly) pairs
    /// covering several reduction shapes:
    ///   - irreducible input (no divisor)
    ///   - single-step reduction
    ///   - chain reduction
    ///   - reduction to zero
    ///   - mid-tail reduction (head is irreducible, but
    ///     a tail term reduces)
    #[test]
    fn geobucket_and_heap_reducer_agree() {
        use crate::sbasis::SBasis;
        let r = mk_ring(3);
        let neg_one = -Fr::one();

        type TermSpec = (Fr, Vec<u32>);
        type PolySpec = Vec<TermSpec>;
        type Case = (Vec<PolySpec>, PolySpec);

        let cases: Vec<Case> = vec![
            // (1) Empty basis: lobject = x + y + z reduces to itself.
            (
                vec![],
                vec![
                    (Fr::one(), vec![1, 0, 0]),
                    (Fr::one(), vec![0, 1, 0]),
                    (Fr::one(), vec![0, 0, 1]),
                ],
            ),
            // (2) basis = {x}, lobject = x + y + z → reduces to y + z.
            (
                vec![vec![(Fr::one(), vec![1, 0, 0])]],
                vec![
                    (Fr::one(), vec![1, 0, 0]),
                    (Fr::one(), vec![0, 1, 0]),
                    (Fr::one(), vec![0, 0, 1]),
                ],
            ),
            // (3) basis = {x - y}, lobject = x → reduces to y
            // (after subtracting 1*(x-y) = x - y, we get y).
            (
                vec![vec![(Fr::one(), vec![1, 0, 0]), (neg_one, vec![0, 1, 0])]],
                vec![(Fr::one(), vec![1, 0, 0])],
            ),
            // (4) basis = {x - y, y - z}, lobject = x → chain → z.
            (
                vec![
                    vec![(Fr::one(), vec![1, 0, 0]), (neg_one, vec![0, 1, 0])],
                    vec![(Fr::one(), vec![0, 1, 0]), (neg_one, vec![0, 0, 1])],
                ],
                vec![(Fr::one(), vec![1, 0, 0])],
            ),
            // (5) basis = {x - 1}, lobject = x^3 - 1 → reduces to 0.
            (
                vec![vec![(Fr::one(), vec![1, 0, 0]), (neg_one, vec![0, 0, 0])]],
                vec![(Fr::one(), vec![3, 0, 0]), (neg_one, vec![0, 0, 0])],
            ),
            // (6) basis = {x*y - z}, lobject = x*y*z → tail reduction.
            (
                vec![vec![(Fr::one(), vec![1, 1, 0]), (neg_one, vec![0, 0, 1])]],
                vec![(Fr::one(), vec![1, 1, 1])],
            ),
        ];

        for (basis_terms, f_terms) in cases {
            // Build the basis polynomials and load them into an SBasis.
            let basis_polys: Vec<Poly<Fr>> = basis_terms
                .into_iter()
                .map(|terms| {
                    Poly::from_terms(
                        &r,
                        terms.into_iter().map(|(c, e)| (c, mono(&r, &e))).collect(),
                    )
                })
                .collect();
            let mut s_basis = SBasis::<Fr>::new();
            for p in &basis_polys {
                s_basis.insert(&r, p.clone());
            }

            let f = Poly::from_terms(
                &r,
                f_terms
                    .into_iter()
                    .map(|(c, e)| (c, mono(&r, &e)))
                    .collect(),
            );

            // Run both reducers on identical starting state.
            let mut lobj_geo = LObject::from_poly(Arc::clone(&r), f.clone());
            reduce_lobject_geobucket(&mut lobj_geo, &s_basis, &r);
            let geo_result = lobj_geo.into_poly();

            let mut lobj_heap = LObject::from_poly(Arc::clone(&r), f.clone());
            reduce_lobject_heap(&mut lobj_heap, &s_basis, &r);
            let heap_result = lobj_heap.into_poly();

            assert_eq!(
                geo_result, heap_result,
                "reducers disagree on f = {f:?} with basis_polys = {basis_polys:?}: \
                 geobucket = {geo_result:?}, heap = {heap_result:?}"
            );
        }
    }

    /// Sanity check that `find_sev_match` (the dispatcher chosen at
    /// compile time based on `target_feature = "avx2"`) produces the
    /// same answer as `find_sev_match_scalar` for a range of inputs
    /// that exercise the unrolled main loop, the 4-wide tail, and
    /// the scalar tail. ADR-007 (extracted to `crate::simd` in ADR-009).
    #[test]
    fn find_sev_match_simd_matches_scalar() {
        use crate::simd::find_sev_match_scalar;
        // Generate a deterministic pseudo-random sev array large
        // enough to span all three loop bodies in the AVX2 path
        // (16-batch main, 4-batch tail, scalar tail). Use a basic LCG
        // so the test has no external dependencies.
        let len = 200; // 12 main batches (192 entries) + 1 four-batch + 4 scalar
        let mut sevs: Vec<u64> = Vec::with_capacity(len);
        let mut state: u64 = 0x1234_5678_9abc_def0;
        for _ in 0..len {
            state = state
                .wrapping_mul(6364136223846793005)
                .wrapping_add(1442695040888963407);
            sevs.push(state);
        }

        // Try several `not_sev` patterns that produce different match
        // densities — all-zero (every entry matches), all-set (no
        // entry matches unless its sev is zero), and a sparse mask.
        for not_sev in [
            0u64,
            !0u64,
            0x0000_0000_FFFF_FFFFu64,
            0xAAAA_AAAA_5555_5555u64,
            0x0000_0001_0000_0001u64,
        ] {
            // Try several start indices to exercise non-aligned entry
            // points into the main loop and partial-batch tails.
            for start in [0usize, 1, 7, 16, 17, 32, 96, 192, 196, 199] {
                let scalar = find_sev_match_scalar(&sevs, not_sev, start);
                let dispatch = find_sev_match(&sevs, not_sev, start);
                assert_eq!(
                    scalar, dispatch,
                    "mismatch at not_sev = {not_sev:#x}, start = {start}: \
                     scalar said {scalar}, dispatch said {dispatch}"
                );
            }
        }

        // Also explicitly check empty / single-element edge cases.
        assert_eq!(find_sev_match(&[], 0u64, 0), 0);
        assert_eq!(find_sev_match(&[5u64], 0u64, 0), 0);
        assert_eq!(find_sev_match(&[5u64], !0u64, 0), 1);
        assert_eq!(find_sev_match(&[0u64, 1u64], 1u64, 0), 0);
        assert_eq!(find_sev_match(&[0u64, 1u64], 1u64, 1), 2);
    }

